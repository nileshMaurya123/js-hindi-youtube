// while loop example

let i = 1; // initialize variable
while (i <= 5) {
  // condition
  console.log(i); // print current value of i
  i++; // increment i
}

// do while loop syntax

//do {
// Code to execute at least once
//} while (condition);

// do while loop example

let j = 1; // initialize variable
do {
  console.log(j); // print current value of j
  j++; // increment j
} while (j <= 5); // condition
